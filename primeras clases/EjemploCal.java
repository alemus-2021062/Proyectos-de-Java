public class EjemploCal{
	public static void main(String args[]){
	int numero;
	numero=500; //asignando el valor 
	numero =numero + 300; //suma
	System.out.println(numero);
	numero=numero/2; //division
	System.out.println(numero);
	numero=numero-150; //resta
	System.out.println(numero);
	numero=numero*2; //multiplicacion
	System.out.println(numero);
	numero=numero%17; //residuo
	System.out.println(numero);
	
	}




}